    public static int ReplaceCost(String w1, String w2, int w1Index, int w2Index) 
    {
    	int start = (int)w1.charAt(w1Index) - 'a';
    	int end = (int)w2.charAt(w2Index) - 'a';
    	
    	if( start == end ) return( 0 );
    	
    	bVisited = new boolean[26];

    	Queue<Integer> objQueue = new LinkedList<Integer>();
    	objQueue.add( start );
		bVisited[start] = true;
    	return( ReplaceCost( end, 0, objQueue ) );
	}
    
	static boolean [] bVisited;
	
    static int ReplaceCost( int end, int count, Queue<Integer> objInQueue )
    {
		count++;
    	Queue<Integer> objOutQueue = new LinkedList<Integer>();

    	for( int node : objInQueue )
    	{
			for (int i = 0; i < 26; i++)
			{
				if (matrix[node][i]!=0 && !bVisited[i])
				{
					if( i == end ) return( count );
					bVisited[i] = true;
					objOutQueue.add(i);
				}
			}
    	}

    	return( ReplaceCost( end, count, objOutQueue ) );
    }

    public static int min(int... numbers) 
    {
    	int result = Integer.MAX_VALUE;
    	for (int each : numbers) 
    	{
    		result = Math.min(result, each);
    	}

        return result;
    }
    
